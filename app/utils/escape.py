def escape(text: str) -> str:
    return text.translate(str.maketrans({
        "_": "\_",
        "*": "\*",
        "[": "\[",
        "]": "\]",
        "(": "\(",
        ")": "\)",
        "~": "\~",
        "`": "\`",
        ">": "\>",
        "#": "\#",
        "+": "\+",
        "-": "\-",
        "=": "\=",
        "|": "\|",
        "{": "\{",
        "}": "\}",
        ".": "\.",
        "!": "\!"
    }))
